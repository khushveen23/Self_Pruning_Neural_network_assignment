"""
Adaptive Weight Masking Network
A neural network that learns to eliminate redundant connections during training.
"""

import torch
import torch.nn as nn
import torch.optim as optim
from torch.utils.data import DataLoader
import torchvision
import torchvision.transforms as transforms
import numpy as np
import matplotlib.pyplot as plt
from tqdm import tqdm


class MaskedLinear(nn.Module):
    """
    Linear layer augmented with trainable binary masks for automatic connection pruning.

    A continuous relaxation of hard masks is used: each connection is associated
    with a learnable score that gets squashed through sigmoid. Scores pushed high
    keep the connection alive; scores driven low effectively zero out the weight.
    """

    def __init__(self, in_features, out_features):
        super(MaskedLinear, self).__init__()
        self.in_features = in_features
        self.out_features = out_features

        # Learnable weight matrix and bias term
        self.weight = nn.Parameter(torch.randn(out_features, in_features) * 0.01)
        self.bias = nn.Parameter(torch.zeros(out_features))

        # Continuous mask logits — initialised positive so all connections start open
        self.mask_logits = nn.Parameter(torch.ones(out_features, in_features) * 2.0)

    def forward(self, x):
        """
        Compute masked linear transformation.

        Multiplies each weight by its soft mask value before the matrix multiply,
        so connections with near-zero masks contribute almost nothing to the output.

        Args:
            x: Input tensor of shape (batch_size, in_features)

        Returns:
            Output tensor of shape (batch_size, out_features)
        """
        masks = torch.sigmoid(self.mask_logits)

        # Element-wise masking zeroes out low-scoring connections
        effective_weights = self.weight * masks

        # Standard affine transform: y = xW^T + b
        return torch.nn.functional.linear(x, effective_weights, self.bias)

    def get_masks(self):
        """Return current soft mask values in [0, 1]."""
        return torch.sigmoid(self.mask_logits)


class AdaptivePruningNet(nn.Module):
    """
    Feed-forward classifier for CIFAR-10 with built-in connection pruning.

    During training the network jointly optimises classification accuracy
    and a sparsity objective, learning which connections are dispensable.
    """

    def __init__(self, input_size=3072, hidden_sizes=[512, 256, 128], num_classes=10):
        super(AdaptivePruningNet, self).__init__()

        self.flatten = nn.Flatten()

        layers = []
        prev_size = input_size

        for hidden_size in hidden_sizes:
            layers.append(MaskedLinear(prev_size, hidden_size))
            layers.append(nn.ReLU())
            prev_size = hidden_size

        layers.append(MaskedLinear(prev_size, num_classes))

        self.network = nn.Sequential(*layers)

        # Keep a direct reference to masked layers for easy access
        self.masked_layers = [l for l in self.network if isinstance(l, MaskedLinear)]

    def forward(self, x):
        x = self.flatten(x)
        return self.network(x)

    def compute_sparsity_loss(self):
        """
        Aggregate L1 penalty across all mask values.

        Summing sigmoid outputs applies constant downward pressure on every
        mask, incentivising the network to close unnecessary connections.

        Returns:
            Scalar tensor representing the total mask activation.
        """
        sparsity_loss = 0.0
        for layer in self.masked_layers:
            masks = layer.get_masks()
            sparsity_loss += masks.sum()

        return sparsity_loss

    def get_sparsity_stats(self, threshold=1e-2):
        """
        Report how many connections have been effectively pruned.

        Args:
            threshold: Mask values below this cutoff are treated as pruned.

        Returns:
            Dictionary containing counts and the full mask value array.
        """
        total_weights = 0
        pruned_weights = 0
        all_masks = []

        for layer in self.masked_layers:
            masks = layer.get_masks().detach().cpu().numpy()
            all_masks.append(masks.flatten())

            total_weights += masks.size
            pruned_weights += (masks < threshold).sum()

        sparsity_percentage = (pruned_weights / total_weights) * 100
        all_masks = np.concatenate(all_masks)

        return {
            'total_weights': total_weights,
            'pruned_weights': pruned_weights,
            'active_weights': total_weights - pruned_weights,
            'sparsity_percentage': sparsity_percentage,
            'all_gates': all_masks          # key preserved for downstream compatibility
        }


