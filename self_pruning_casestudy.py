def load_cifar10(batch_size=128):
    """Download and prepare CIFAR-10 train / test loaders with mean-std normalisation."""
    transform = transforms.Compose([
        transforms.ToTensor(),
        transforms.Normalize((0.5, 0.5, 0.5), (0.5, 0.5, 0.5))
    ])

    trainset = torchvision.datasets.CIFAR10(root='./data', train=True,
                                            download=True, transform=transform)
    trainloader = DataLoader(trainset, batch_size=batch_size,
                             shuffle=True, num_workers=2)

    testset = torchvision.datasets.CIFAR10(root='./data', train=False,
                                           download=True, transform=transform)
    testloader = DataLoader(testset, batch_size=batch_size,
                            shuffle=False, num_workers=2)

    return trainloader, testloader


# ---------------------------------------------------------------------------
# Training & evaluation helpers
# ---------------------------------------------------------------------------

def train_epoch(model, trainloader, optimizer, criterion, lambda_sparsity, device):
    """Run one full pass over the training set and return aggregated metrics."""
    model.train()
    running_loss = 0.0
    running_class_loss = 0.0
    running_sparsity_loss = 0.0
    correct = 0
    total = 0

    for inputs, labels in trainloader:
        inputs, labels = inputs.to(device), labels.to(device)

        optimizer.zero_grad()

        outputs = model(inputs)

        # Task loss: cross-entropy over 10 CIFAR classes
        class_loss = criterion(outputs, labels)

        # Regularisation loss: push mask values toward zero
        sparsity_loss = model.compute_sparsity_loss()

        # Weighted combination drives the sparsity-accuracy trade-off
        total_loss = class_loss + lambda_sparsity * sparsity_loss

        total_loss.backward()
        optimizer.step()

        running_loss += total_loss.item()
        running_class_loss += class_loss.item()
        running_sparsity_loss += sparsity_loss.item()

        _, predicted = outputs.max(1)
        total += labels.size(0)
        correct += predicted.eq(labels).sum().item()

    n = len(trainloader)
    return {
        'loss': running_loss / n,
        'class_loss': running_class_loss / n,
        'sparsity_loss': running_sparsity_loss / n,
        'accuracy': 100. * correct / total
    }


def evaluate(model, testloader, criterion, device):
    """Measure loss and accuracy on the held-out test split."""
    model.eval()
    test_loss = 0.0
    correct = 0
    total = 0

    with torch.no_grad():
        for inputs, labels in testloader:
            inputs, labels = inputs.to(device), labels.to(device)
            outputs = model(inputs)
            loss = criterion(outputs, labels)

            test_loss += loss.item()
            _, predicted = outputs.max(1)
            total += labels.size(0)
            correct += predicted.eq(labels).sum().item()

    return {
        'loss': test_loss / len(testloader),
        'accuracy': 100. * correct / total
    }


# ---------------------------------------------------------------------------
# Main training loop
# ---------------------------------------------------------------------------

def train_model(lambda_sparsity, num_epochs=50, device='cuda'):
    """
    Instantiate and train an AdaptivePruningNet for a given sparsity weight.

    Args:
        lambda_sparsity: Scalar multiplier for the mask regularisation term.
        num_epochs: Total number of training epochs.
        device: Torch device string ('cuda' or 'cpu').

    Returns:
        Tuple of (trained model, history dict, final sparsity stats dict).
    """
    print(f"\n{'='*60}")
    print(f"  Training run  |  λ = {lambda_sparsity}")
    print(f"{'='*60}")

    trainloader, testloader = load_cifar10()

    model = AdaptivePruningNet().to(device)

    criterion = nn.CrossEntropyLoss()
    optimizer = optim.Adam(model.parameters(), lr=0.001)

    # Halve the learning rate every 20 epochs to stabilise late-stage training
    scheduler = optim.lr_scheduler.StepLR(optimizer, step_size=20, gamma=0.5)

    history = {
        'train_loss': [],
        'train_acc': [],
        'test_loss': [],
        'test_acc': [],
        'sparsity': []
    }

    best_acc = 0.0

    for epoch in range(num_epochs):
        train_stats = train_epoch(model, trainloader, optimizer, criterion,
                                  lambda_sparsity, device)
        test_stats = evaluate(model, testloader, criterion, device)
        sparsity_stats = model.get_sparsity_stats()

        history['train_loss'].append(train_stats['loss'])
        history['train_acc'].append(train_stats['accuracy'])
        history['test_loss'].append(test_stats['loss'])
        history['test_acc'].append(test_stats['accuracy'])
        history['sparsity'].append(sparsity_stats['sparsity_percentage'])

        scheduler.step()

        if (epoch + 1) % 5 == 0 or epoch == 0:
            print(f"  Epoch {epoch+1:>3}/{num_epochs}")
            print(f"    Train — Loss: {train_stats['loss']:.4f}  Acc: {train_stats['accuracy']:.2f}%")
            print(f"    Test  — Loss: {test_stats['loss']:.4f}  Acc: {test_stats['accuracy']:.2f}%")
            print(f"    Sparsity: {sparsity_stats['sparsity_percentage']:.2f}%")

        if test_stats['accuracy'] > best_acc:
            best_acc = test_stats['accuracy']

    final_sparsity = model.get_sparsity_stats()
    print(f"\n  ── Final Metrics ──────────────────────────")
    print(f"  Test Accuracy  : {test_stats['accuracy']:.2f}%")
    print(f"  Sparsity       : {final_sparsity['sparsity_percentage']:.2f}%")
    print(f"  Active Weights : {final_sparsity['active_weights']} / {final_sparsity['total_weights']}")

    return model, history, final_sparsity


