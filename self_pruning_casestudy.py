def plot_gate_distribution(model, lambda_val, save_path='gate_distribution.png'):
    """
    Render a styled histogram of all mask values across the network.

    A bimodal distribution (mass near 0 and near 1) indicates successful pruning:
    the network has committed each connection to either active or dormant.
    """
    stats = model.get_sparsity_stats()
    masks = stats['all_gates']

    fig, ax = plt.subplots(figsize=(10, 5))

    # Teal-themed bars with a slightly darker edge for legibility
    ax.hist(masks, bins=100, color='#2a9d8f', edgecolor='#1b6b62', alpha=0.85)

    ax.set_xlabel('Mask Activation Value', fontsize=13, labelpad=8)
    ax.set_ylabel('Count', fontsize=13, labelpad=8)
    ax.set_title(
        f'Connection Mask Distribution  (λ = {lambda_val})',
        fontsize=15, fontweight='bold', pad=14
    )

    # Subtle grid on y-axis only
    ax.yaxis.grid(True, linestyle='--', linewidth=0.6, alpha=0.5, color='#cccccc')
    ax.set_axisbelow(True)
    ax.spines['top'].set_visible(False)
    ax.spines['right'].set_visible(False)

    fig.tight_layout()
    fig.savefig(save_path, dpi=300, bbox_inches='tight')
    print(f"  Saved: {save_path}")
    plt.close(fig)


